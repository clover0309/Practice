import java.util.Arrays;

public class 연산자 {
    public static void main(String[] args) {
        //산술 연산자
        int num1 = 8, num2 = 4;

        System.out.println("+ : " + (num1 + num2)); // 8+4 = 12
        System.out.println("- : " + (num1 - num2)); // 8-4 = 4
        System.out.println("* : " + (num1 * num2)); // 8*4 = 32
        System.out.println("/ : " + (num1 / num2)); // 8/4 = 2
        System.out.println("% : " + (num1 % num2)); // 8%4 = 0

    }
}
